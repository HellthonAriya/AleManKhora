/* ------------------------------ building blocks --------------------------- */
function sec(title, ...nodes) {
  return h('div', { class: 'rules-section' }, h('h3', { class: 'rules-h' }, title), ...nodes);
}
function p(...kids) { return h('p', { class: 'rules-p' }, ...kids); }
function ul(items) { return h('ul', { class: 'rules-ul' }, ...items.map((it) => h('li', {}, it))); }
function piece(glyph, name, how) {
  return h('div', { class: 'rule-piece' },
    h('span', { class: 'rp-glyph' }, glyph),
    h('div', {}, h('b', {}, name), h('div', { class: 'rp-how' }, how)));
}
function tip(text) { return h('div', { class: 'rules-tip' }, '💡 ', text); }

