const RENDERERS = { quoridor: quoridorRules, chess: chessRules, chess4: chess4Rules };

/** Open the rules modal for a given game type. */
export function openRules(gameType = 'quoridor') {
  const render = RENDERERS[gameType] || RENDERERS.quoridor;
  modal({
    title: GAME_TITLES[gameType] || GAME_TITLES.quoridor,
    body: render(),
    actions: [{ label: 'فهمیدم!', class: 'btn-primary' }],
  });
}
